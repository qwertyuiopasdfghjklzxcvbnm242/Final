public class coord {
    // Declare variables
    private int x;
    private int y;
    // Constructor
    public coord(int x, int y){
        this.x = x;
        this.y = y;
    }
    /*
    * gets x
    * @return x
    */
    public int getX(){
        return x;
    }
    /*
    * gets y
    * @return y
    */
    public int getY(){
        return y;
    }
}
